package main // исполняемая программа — пакет main

import "fmt" // ввод-вывод: Scan, Println

func main() {
	var n int                  // длина последовательности
	fmt.Scan(&n)               // читаем N
	var x int                  // очередное число
	fmt.Scan(&x)               // читаем первый элемент
	min := x                   // минимум пока равен первому элементу
	count := 1                 // сколько раз встретился текущий минимум (пока один раз)
	for i := 1; i < n; i++ {   // осталось прочитать ещё N−1 чисел
		fmt.Scan(&x)           // читаем следующее число
		if x < min {           // нашли меньшее значение
			min = x            // обновляем минимум
			count = 1          // новый минимум встретился один раз
		} else if x == min {   // значение равно минимуму
			count++            // увеличиваем число вхождений минимума
		}
	}
	fmt.Println(count)         // выводим количество минимумов
}
