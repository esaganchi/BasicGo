package main // исполняемая программа

import "fmt" // Scan, Println

func main() {
	var n int              // исходное натуральное число
	fmt.Scan(&n)           // читаем n из stdin
	for n >= 10 {          // пока не одна цифра — схлопываем суммой цифр
		s := 0             // сумма цифр текущего числа
		for x := n; x > 0; x /= 10 { // перебираем цифры справа налево
			s += x % 10      // добавляем младшую цифру
		}
		n = s              // переходим к следующему «слою»
	}
	fmt.Println(n)         // цифровой корень — одна цифра
}
