// Палиндром (Stepik): одна строка строчными буквами → «Палиндром» или «Нет».
// Идея: два индекса сходятся к центру; при первом расхождении сразу «Нет» и выход.
package main // исполняемая программа

import (
	"bufio"   // чтение строки целиком до '\n'
	"fmt"     // печать ответа
	"os"      // стандартный ввод
	"strings" // обрезка конца строки у прочитанного текста
)

func main() { // точка входа
	text, _ := bufio.NewReader(os.Stdin).ReadString('\n') // читаем stdin до перевода строки (в text обычно есть '\n' в конце)
	text = strings.TrimRight(text, "\r\n")                // убираем \n и \r — сравниваем только «содержимое» строки
	r := []rune(text)                                     // срез рун: посимвольно по Unicode, а не по байтам UTF-8

	for i, j := 0, len(r)-1; i < j; i, j = i+1, j-1 { // i с начала, j с конца; пока не встретились в середине
		if r[i] != r[j] { // симметричные символы должны совпадать
			fmt.Println("Нет") // не палиндром — сразу ответ и выход из main
			return             // дальнейшая печать «Палиндром» не выполнится
		}
	}
	fmt.Println("Палиндром") // цикл завершился без расхождений — строка симметрична (в т.ч. пустая или из одной буквы)
}
