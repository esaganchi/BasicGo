package main // объявление пакета: точка входа в программу

import "fmt" // пакет для вывода в консоль (Println и др.)

func main() { // главная функция, выполняется при запуске
	a := [5]int{5, 10, 15, 20, 25} // массив из 5 int с заданными значениями
	fmt.Println(a)                  // выводим весь массив: [5 10 15 20 25]

	// range копирует значение в elem — оригинал не меняется
	for _, elem := range a { // перебираем массив: _ — индекс (игнорируем), elem — копия элемента
		elem = 100        // меняем только локальную копию, не элемент массива
		fmt.Println(elem) // печатаем 100 пять раз
	}
	fmt.Println(a) // массив a не изменился: [5 10 15 20 25]

	// обращение по индексу меняет сам массив
	for idx := range a { // перебираем индексы 0, 1, 2, 3, 4
		a[idx] = 100        // записываем 100 в элемент массива по индексу idx
		fmt.Println(a[idx]) // печатаем значение элемента (100)
	}
	fmt.Println(a) // массив изменился: [100 100 100 100 100]
}
