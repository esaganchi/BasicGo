// Таблица умножения N×M: в ячейке (i, j) — произведение i*j (i, j с 1).
// Ввод: два числа N, M (1 ≤ N, M ≤ 10). Вывод: числа через пробел, без пробела в конце строки.
package main

import (
	"fmt"     // вывод и чтение
	"strings" // Join для склейки строки без пробела в конце
)

func main() {
	var n, m int
	fmt.Println("Введите два числа N и M (1 ≤ N, M ≤ 10):")
	fmt.Scan(&n, &m) // считываем количество строк и столбцов

	for i := 1; i <= n; i++ { // перебираем строки (нумерация с 1)
		row := make([]string, m)  // слайс из m строк под числа текущей строки
		for j := 1; j <= m; j++ { // перебираем столбцы (нумерация с 1)
			row[j-1] = fmt.Sprint(i * j) // в ячейку (i,j) пишем i*j; индекс в слайсе на 1 меньше
		}
		fmt.Println(strings.Join(row, " ")) // выводим строку: числа через пробел, без пробела в конце
	}
}
