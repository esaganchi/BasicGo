// Нечётные индексы (с нуля): из входной строки — только руны с индексами 1, 3, 5, …
package main // исполняемая программа

import (
	"bufio"   // чтение строки из stdin до символа '\n'
	"fmt"     // печать результата
	"os"      // стандартный ввод
	"strings" // TrimRight — убрать конец строки; Builder — собрать ответ по частям
)

func main() { // точка входа
	text, _ := bufio.NewReader(os.Stdin).ReadString('\n') // читаем одну строку (в конце обычно есть '\n')
	text = strings.TrimRight(text, "\r\n")                // убираем перевод строки, чтобы индексы считались по «слову»
	fmt.Println(oddIndexChars(text))                      // печатаем строку из рун с нечётными индексами 1,3,5,…
}

// oddIndexChars собирает новую строку из рун входной строки с нечётными индексами (1, 3, 5, …).
func oddIndexChars(s string) string {
	r := []rune(s)                   // срез рун: индексы — по символам Unicode, а не по байтам UTF-8
	var b strings.Builder            // накопитель символов результата (эффективнее, чем += строки в цикле)
	for i := 1; i < len(r); i += 2 { // индексы 1, 3, 5, … — «нечётные» при счёте с нуля
		b.WriteRune(r[i]) // дописываем очередную руну результата
	}
	return b.String() // готовая строка из Builder
}
