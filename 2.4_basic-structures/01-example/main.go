package main // пакет main — единственный пакет, из которого собирают исполняемую программу

import (
	"fmt"  // форматированный вывод в консоль (Println и др.)
	"math" // константа Pi и математические функции
)

// Circle описывает окружность: центр (x, y) и радиус r.
type Circle struct {
	x, y, r float64 // все поля — вещественные числа float64
}

// Rectangle — прямоугольник, заданный двумя противоположными углами (x1,y1) и (x2,y2).
type Rectangle struct {
	x1, y1, x2, y2 float64 // координаты углов на плоскости
}

// area — метод типа Circle: приёмник c — конкретный экземпляр круга; возвращает площадь float64.
func (c Circle) area() float64 {
	return math.Pi * c.r * c.r // π·r² — площадь круга; c.r — радиус из приёмника
}

// area — метод типа Rectangle; приёмник r здесь — это прямоугольник (имя переменной), не радиус.
func (r Rectangle) area() float64 {
	l := r.x2 - r.x1 // «длина» по оси X (разность абсцисс углов)
	w := r.y2 - r.y1 // «ширина» по оси Y (разность ординат)
	return l * w     // площадь осесимметричного прямоугольника: произведение сторон
}

func main() {
	rect := Rectangle{x1: 0, y1: 0, x2: 10, y2: 10} // литерал структуры: углы (0,0) и (10,10)
	circ := Circle{x: 0, y: 0, r: 5}                // центр в начале координат, радиус 5

	fmt.Println("Rectangle area:", rect.area()) // вызываем метод area у значения rect
	fmt.Println("Circle area:", circ.area())    // вызываем метод area у значения circ
}
