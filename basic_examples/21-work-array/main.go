package main // объявление исполняемого пакета

import "fmt" // пакет для ввода-вывода

func main() { // точка входа в программу
	var workArray [10]uint8 // массив из 10 беззнаковых 8-битных целых

	// 1. Читаем 10 чисел в массив
	for i := 0; i < 10; i++ { // цикл по индексам 0..9
		fmt.Scan(&workArray[i]) // читаем число в i-й элемент массива
	}

	// 2. Читаем 3 пары индексов и меняем элементы местами
	var idx1, idx2 int // индексы для обмена
	for i := 0; i < 3; i++ { // три пары индексов
		fmt.Scan(&idx1, &idx2) // читаем пару индексов
		workArray[idx1], workArray[idx2] = workArray[idx2], workArray[idx1] // обмен значений по индексам
	}

	// 3. Выводим результат с пробелом после каждого числа
	for _, v := range workArray { // перебираем элементы (индекс не нужен)
		fmt.Printf("%d ", v) // выводим число и пробел
	}
} // конец main
