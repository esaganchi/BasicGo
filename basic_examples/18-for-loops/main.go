package main

import "fmt"

// main — точка входа: при запуске программы выполняется только она.
// Здесь мы вызываем main6(), чтобы запустить задачу «первое число, кратное c и не кратное d».
func main() {
	main6()
}

// main1: сумма всех чисел от A до B включительно (A, B ≤ 100, A < B)
func main1() {
	var a, b int
	fmt.Print("Введите A и B: ")
	fmt.Scan(&a, &b)

	if a >= b || a > 100 || b > 100 {
		fmt.Println("Ошибка: нужны натуральные A и B ≤ 100, при этом A < B")
		return
	}

	sum := 0
	for i := a; i <= b; i++ { // цикл от A до B включительно
		sum += i
	}
	fmt.Println(sum)
}

// main2: сумма двузначных чисел, кратных 8 (в первой строке n, затем n чисел)
func main2() {
	var n int
	fmt.Print("Введите количество чисел n: ")
	fmt.Scan(&n)

	sum := 0
	for i := 0; i < n; i++ {
		var num int
		fmt.Scan(&num)
		if num >= 10 && num <= 99 && num%8 == 0 { // двузначное и кратно 8
			sum += num
		}
	}
	fmt.Println(sum)
}

// main3: последовательность до 0 — сколько элементов равны максимуму
func main3() {
	var num int
	fmt.Scan(&num) // читаем первое число
	if num == 0 {
		fmt.Println(0) // пустая последовательность
		return
	}

	max := num // пока максимум — первое число
	count := 1 // встретился один раз

	for {
		fmt.Scan(&num)
		if num == 0 {
			break // конец последовательности — выходим из цикла
		}
		if num > max {
			max = num // новый максимум
			count = 1 // сбрасываем счётчик
		} else if num == max {
			count++ // ещё одно число, равное максимуму
		}
	}
	fmt.Println(count) // ответ: сколько раз встретился максимум
}

// main4: пример continue — сумма нечётных от 1 до 10
func main4() {
	sum := 0
	for i := 1; i <= 10; i++ {
		if i%2 == 0 {
			continue
		}
		sum += i
	}
	fmt.Println(sum) // 1+3+5+7+9 = 25
}

// main5: пример break
func main5() {
	sum := 0
	for i := 1; i <= 9; i++ {
		if i > 4 {
			break
		}
		sum += i
	}
	fmt.Println("Сумма:", sum)
}

// main6: первое число от 1 до n включительно, кратное c, но НЕ кратное d
func main6() {
	// Объявляем три переменные под входные данные: верхняя граница n, делитель c, «исключающий» делитель d
	var n, c, d int
	// Читаем из ввода одну строку с тремя числами и записываем их в n, c, d
	fmt.Scan(&n, &c, &d)

	// Перебираем числа i от 1 до n включительно
	for i := 1; i <= n; i++ {
		// i подходит, если делится на c без остатка и при этом НЕ делится на d
		if i%c == 0 && i%d != 0 {
			// Нашли первое подходящее число — выводим и сразу выходим из функции
			fmt.Println(i)
			return
		}
	}
	// Если цикл закончился и мы здесь — подходящего числа нет; по условию ничего не выводим
}

// main7: читаем целые числа по одному в строке; < 10 — пропуск, > 100 — конец ввода, иначе выводим число
func main7() {
	var num int // переменная для очередного введённого числа
	for {       // бесконечный цикл, выходим по break
		_, err := fmt.Scanln(&num) // читаем одно число с консоли (до перевода строки)
		if err != nil {            // если ввод закончился или ошибка — выходим из цикла
			break
		}
		if num < 10 { // число меньше 10 — ничего не выводим, переходим к следующей итерации
			continue
		}
		if num > 100 { // число больше 100 — прекращаем считывать и выходим из цикла
			break
		}
		fmt.Println(num) // иначе (от 10 до 100) — выводим число в отдельной строке
	}
}

// main8: вклад x рублей, каждый год +p%, дробная часть копеек отбрасывается; через сколько лет вклад ≥ y
func main8() {
	var x, p, y int
	fmt.Scan(&x, &p, &y)

	// считаем в копейках, чтобы целочисленно отбрасывать дробную часть после начисления процентов
	amount := x * 100 // текущий вклад в копейках
	target := y * 100 // цель в копейках
	years := 0        // прошло лет

	for amount < target {
		// начисляем p%: новое = старое * (100 + p) / 100; целочисленное деление отбрасывает копейки
		amount = amount * (100 + p) / 100
		years++
	}
	fmt.Println(years)
}
