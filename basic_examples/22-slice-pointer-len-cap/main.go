// Пример 1: на вход N и N целых чисел, на вывод — 4-й элемент среза (индекс 3).
func example1SliceFourthElement() {
	var n int    // количество элементов среза
	fmt.Scan(&n) // читаем N из stdin

	a := make([]int, n) // срез из n целых (индексы 0..n-1)
	for i := 0; i < n; i++ {
		fmt.Scan(&a[i]) // читаем число в a[i]
	}

	fmt.Println(a[3]) // выводим 4-й элемент (индекс 3)
}

// Пример 2: на вход 5 целых чисел в массив, на вывод — максимальное число.
func example2MaxInArray() {
	array := [5]int{} // массив из 5 целых (изначально нули)
	var a int         // переменная для чтения одного числа
	for i := 0; i < 5; i++ {
		fmt.Scan(&a) // читаем число из stdin
		array[i] = a // записываем в массив по индексу i
	}

	max := array[0] // пока максимум — первый элемент
	for i := 1; i < 5; i++ {
		if array[i] > max {
			max = array[i] // нашли больше — обновляем максимум
		}
	}
	fmt.Println(max) // выводим результат
}

// Пример 3: на вход N и N целых чисел, на вывод — элементы с чётными индексами (0, 2, 4...) через пробел.
func example3EvenIndices() {
	var n int    // количество элементов
	fmt.Scan(&n) // читаем N из stdin

	a := make([]int, n) // срез из n целых
	for i := 0; i < n; i++ {
		fmt.Scan(&a[i]) // читаем элемент в a[i]
	}

	for i := 0; i < n; i += 2 { // идём по чётным индексам: 0, 2, 4...
		if i > 0 {
			fmt.Print(" ") // пробел перед элементом (кроме первого)
		}
		fmt.Print(a[i]) // выводим элемент без перевода строки
	}
	fmt.Println() // перевод строки в конце вывода
}

// Пример 4: на вход N и N целых чисел, на вывод — количество положительных элементов.
func example4CountPositive() {
	var n int           // количество элементов последовательности
	fmt.Scan(&n)        // читаем N из stdin

	a := make([]int, n) // срез из n целых
	for i := 0; i < n; i++ {
		fmt.Scan(&a[i]) // читаем элемент в a[i]
	}

	count := 0              // счётчик положительных (пока ноль)
	for i := 0; i < n; i++ {
		if a[i] > 0 {
			count++ // элемент положительный — увеличиваем счётчик
		}
	}
	fmt.Println(count) // выводим результат
}
